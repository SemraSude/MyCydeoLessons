package day03_Operators;

public class UnaryOperators {
    public static void main(String[] args) {

        System.out.println(+55);
        System.out.println(-55);

        int wood=5;
       wood++;

        System.out.println(wood);
        System.out.println(wood--);
        System.out.println(wood);

        wood--;
        System.out.println(wood);


    }
}
