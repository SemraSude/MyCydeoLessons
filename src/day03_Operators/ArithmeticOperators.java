package day03_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {

       int num1=3;
       int num2=10;
       double num3=30.0;

        System.out.println(10+5);
        System.out.println(10-5);
        System.out.println(10*5);
        System.out.println(10*num1);
        System.out.println(num1-num2);

        System.out.println(38 / 10);// whole numbers, we only take the whole part
        System.out.println( num1 / num2);

        System.out.println(38.0 / 5);
        System.out.println(48 / 10.0);

        System.out.println(38 % 5);
        System.out.println(19 % 9); // do the division, keep the result for yourself, gimme the remaining part






    }
}
